import re


def search_number_phone(text):
    """
    Пошук усіх номерів телефонів у будь-якому форматі та передача для перевірки унікальності.

    :param text: Вхідний текст, у якому здійснюється пошук номерів телефонів.
    :type text: str
    """
    # Знаходимо всі номери в будь-якому форматі
    phones = re.findall(r"\(?\d{3}\)?[ .-]?\d{3}[ .-]?\d{4}", text)
    validata_phones(phones)


def validata_phones(phones):
    """
    Перевірка та форматування унікальних номерів телефонів зі списку.

    :param phones: Список знайдених номерів телефонів.
    :type phones: list
    """
    unique_phones = set()
    for phone in phones:
        formatted_phone = re.sub(r"\D", "", phone)  # Видаляємо всі нецифрові символи
        if len(formatted_phone) == 10 and formatted_phone not in unique_phones:
            unique_phones.add(formatted_phone)
            # Форматуємо номер, вставляючи потрібні дужки та дефіс
            formatted_phone = re.sub(r"(\d{3})(\d{3})(\d{4})", r"(\1) \2-\3", formatted_phone)
            print(f"Номер телефону: {formatted_phone}")


